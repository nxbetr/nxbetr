package co.yedam.var;

public class Example {

	public static void main(String[] args) {
		// 반지름이 5인 원의 면젹
		
		double PI = 3.14;
		int radius = 5;
		double gongsic = PI * radius * radius;
		System.out.println(3.14 * 5 * 5);
		System.out.println(gongsic);
		
		// 덧셈 + 뺄셈 - 곱셈 * 나눗셈 /
		
		// 한 면이 4인 삼각형의 넓이를 구하라
		// 밑변 * 높이 /2
		int under = 4;
		int height = 4;
		double gongsic2 = under * height / 2;
		// 8.0 출력
		// int로 쓰면 8 로 출력
		System.out.println(gongsic2);
		
		double u = 541.1;
		double h = 123.4;
		double g = u * h / 2;
		System.out.println(g);
		
				
		
	}

}
